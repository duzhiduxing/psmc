package priv.guochun.psmc.authentication.operate.model;

public class TabOperate
{
    private String uuid;
    private String resourceUuid;
    private String privilegeUuid;
    private String funClass;
    private String funMethod;
    private String operateName;
    private String operateDesc;
    private String ordernum;
    private String operateNo;
    public String getUuid()
    {
        return uuid;
    }
    public void setUuid(String uuid)
    {
        this.uuid = uuid;
    }
    public String getResourceUuid()
    {
        return resourceUuid;
    }
    public void setResourceUuid(String resourceUuid)
    {
        this.resourceUuid = resourceUuid;
    }
    public String getPrivilegeUuid()
    {
        return privilegeUuid;
    }
    public void setPrivilegeUuid(String privilegeUuid)
    {
        this.privilegeUuid = privilegeUuid;
    }
    public String getFunClass()
    {
        return funClass;
    }
    public void setFunClass(String funClass)
    {
        this.funClass = funClass;
    }
    public String getFunMethod()
    {
        return funMethod;
    }
    public void setFunMethod(String funMethod)
    {
        this.funMethod = funMethod;
    }
    public String getOperateName()
    {
        return operateName;
    }
    public void setOperateName(String operateName)
    {
        this.operateName = operateName;
    }
    public String getOperateDesc()
    {
        return operateDesc;
    }
    public void setOperateDesc(String operateDesc)
    {
        this.operateDesc = operateDesc;
    }
    public String getOrdernum()
    {
        return ordernum;
    }
    public void setOrdernum(String ordernum)
    {
        this.ordernum = ordernum;
    }
    public String getOperateNo()
    {
        return operateNo;
    }
    public void setOperateNo(String operateNo)
    {
        this.operateNo = operateNo;
    }
}
